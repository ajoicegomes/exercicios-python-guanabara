# Solicita ao usuário que digite um número e armazena em 'n'
n = int(input('Digite um número: '))

# Exibe o dobro do número
print(f'O dobro de {n} vale {n*2}')

# Exibe o triplo do número
print(f'O triplo de {n} vale {n*3}')

# Exibe a raiz quadrada do número
print(f'A raiz quadrada de {n} vale {pow(n, 1/2)}')

